class Koszula extends Ubrania{

    @Override
    public void wypierz(){
         System.out.println("Wyprano koszulę");
    }

    @Override
    public void zaloz(){
        System.out.println("Założono koszulę");
    }

    @Override
    public void wyprasuj(){
        System.out.println("Wyprasuj koszulę");
    }

    @Override
    public void wysusz(){
        System.out.println("Wysuszono koszulę");
    }

    @Override
    public void zniszcz(){
        System.out.println("Zniszczono koszulę");
    }
}
